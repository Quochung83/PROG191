    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            FileWriter fileWriter = new FileWriter("room.txt");
            PrintWriter printWriter = new PrintWriter(fileWriter);

            for (Room room : roomList) {
                // Format the room data as a string and write it to the file
                String roomData = room.getRoomNumber() + "," +
                        room.getFormattedCheckInDate() + "," +
                        room.getFormattedCheckOutDate() + "," +
                        room.getGuestName() + "," +
                        room.getGuestPhoneNumber() + "," +
                        room.getRoomType() + "," +
                        room.getRoomPrice();
                printWriter.println(roomData);
            }

            printWriter.close();
            JOptionPane.showMessageDialog(this, "Data saved successfully to room.txt.", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error saving data to room.txt.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


