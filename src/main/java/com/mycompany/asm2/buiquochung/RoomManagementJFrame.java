    private void updateRoomTable() {
        tableModel.setRowCount(0); // Clear the table

        for (Room room : roomList) {
            Object[] row = {
                    room.getRoomNumber(),
                    room.getFormattedCheckInDate(),
                    room.getFormattedCheckOutDate(),
                    room.getGuestName(),
                    room.getGuestPhoneNumber(),
                    room.getRoomType(),
                    room.getRoomPrice() // You need to implement this method in the Room class
            };
            tableModel.addRow(row);
        }
    }

    private void clearInputFields() {
        roomNumber.setText("");
        checkInDate.setText("");
        checkOutDate.setText("");
        guestName.setText("");
        guestPhoneNumber.setText("");
        jComboBox1.setSelectedIndex(0);
    }

    private void addRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRoomButtonActionPerformed
        try {
            String roomNumberText = roomNumber.getText();
            String checkInDateString = checkInDate.getText();
            String checkOutDateString = checkOutDate.getText();
            String guestNameText = guestName.getText();
            String guestPhoneNumberText = guestPhoneNumber.getText();
            String roomTypeString = (String) jComboBox1.getSelectedItem();

            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            Date checkInDate = dateFormat.parse(checkInDateString);
            Date checkOutDate = dateFormat.parse(checkOutDateString);

            // Sử dụng enum TypeRoom để xác định loại phòng
            Room.TypeRoom roomType = Room.TypeRoom.valueOf(roomTypeString);

            // Tạo phòng dựa trên loại phòng được chọn
            Room room;
            if (roomType == Room.TypeRoom.Luxury) {
                room = new LuxuryRoom(roomNumberText, checkInDate, checkOutDate, guestNameText, guestPhoneNumberText);
            } else {
                room = new NormalRoom(roomNumberText, checkInDate, checkOutDate, guestNameText, guestPhoneNumberText);
            }

            roomList.add(room);
            updateRoomTable();
            clearInputFields();
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Invalid date format. Please use dd/MM/yyyy.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addRoomButtonActionPerformed



    private void updateRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {
        int selectedRow = showListRoomTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a room to update.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            String roomNumberText = roomNumber.getText();
            String checkInDateString = checkInDate.getText();
            String checkOutDateString = checkOutDate.getText();
            String guestNameText = guestName.getText();
            String guestPhoneNumberText = guestPhoneNumber.getText();
            String roomTypeString = (String) jComboBox1.getSelectedItem();

            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            Date checkInDate = dateFormat.parse(checkInDateString);
            Date checkOutDate = dateFormat.parse(checkOutDateString);

            // Sử dụng enum TypeRoom để xác định loại phòng
            Room.TypeRoom roomType = Room.TypeRoom.valueOf(roomTypeString);

            // Lấy phòng đã chọn
            Room selectedRoom = roomList.get(selectedRow);

            // Cập nhật thông tin phòng
            selectedRoom.setRoomNumber(roomNumberText);
            selectedRoom.setCheckInDate(checkInDate);
            selectedRoom.setCheckOutDate(checkOutDate);
            selectedRoom.setGuestName(guestNameText);
            selectedRoom.setGuestPhoneNumber(guestPhoneNumberText);
            selectedRoom.setRoomType(roomType); // Cập nhật loại phòng

            // Tính lại tổng tiền dựa trên thông tin phòng đã cập nhật
            double totalPrice = selectedRoom.calculateTotalPrice();

            // Cập nhật tổng tiền trong mô hình bảng
            showListRoomTable.getModel().setValueAt(roomNumberText,selectedRow, 0);
            showListRoomTable.getModel().setValueAt(checkInDateString, selectedRow, 1);
            showListRoomTable.getModel().setValueAt(checkOutDateString, selectedRow, 2);
            showListRoomTable.getModel().setValueAt(guestNameText, selectedRow, 3);
            showListRoomTable.getModel().setValueAt(guestPhoneNumberText, selectedRow, 4);
            showListRoomTable.getModel().setValueAt(roomTypeString, selectedRow, 5);
            showListRoomTable.getModel().setValueAt(totalPrice, selectedRow, 6);

            clearInputFields();
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Invalid date format. Please use dd/MM/yyyy.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }




    private void deleteRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {
        int selectedRow = showListRoomTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a room to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirmDelete = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this room?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirmDelete == JOptionPane.YES_OPTION) {
            // Remove the selected room from the roomList
            roomList.remove(selectedRow);

            // Update the room table
            updateRoomTable();
            clearInputFields();
        }
    }


